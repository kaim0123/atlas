      <p>機械・産業機械は、明治の輸入依存からNC/CNC技術で世界一へと駆け上がり、いまはファナック・コマツ・キーエンス・ダイキンなど分野ごとの世界的企業を束ねる、日本のものづくりの土台です。「機械を作る機械」を軸にした技術の蓄積が、循環という宿命を抱えながらも高収益を生む源泉になっています。</p>

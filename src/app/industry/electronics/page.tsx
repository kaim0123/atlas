      <p>この産業は、電灯から家電、半導体世界制覇へと駆け上がり、そして凋落を経て「部品・材料・装置」で世界を支える立場へと約150年かけて姿を変えてきました。栄光をもたらした垂直統合・自前主義が、次の時代には敗因に反転した ― 産業史と会計は地続きです。全社平均ではなく、事業ごとの収益性とサイクル局面に分解して読むことが、この複雑な産業を理解する鍵になります。</p>

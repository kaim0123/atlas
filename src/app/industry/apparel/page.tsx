      <p>繊維・アパレルは、明治の生糸輸出から約150年をかけて、生糸・紡績 → 合成繊維 → 空洞化と二極化へと姿を変えてきました。かつての主要産業としての地位は失いましたが、炭素繊維・機能繊維で世界をリードする川上と、SPAで世界展開するユニクロという川下が、それぞれ新しい形の強みを築いています。歴史と会計は地続きで、空洞化の歴史がSPAという業態を生み、在庫と店舗を財務の要にしました。</p>
